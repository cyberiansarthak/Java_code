package CHAPTER_06;
// MULTI DIMENSIONAL ARRAYS
// EACH ELEMENT OF MULTI_D ARRAY IS A ARRAY ITSELF
public class Java_07 {
    public static void main(String[] args) {
        // HOW WE CREATE M_D ARRAY
        int [][] flats = new int [2][3];// DECLARATION AND MEMORY ALLOCATION
        // [2] MEANS THERE ARE 2 ROWS AND [3] MEANS  THERE ARE 3 COLUMNS
        // IF WE WANT TO FIND A11 WE WRITE LIKE THIS
        System.out.println(flats[0][0]);

    }
}
