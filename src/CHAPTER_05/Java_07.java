package CHAPTER_05;
//STAR PATTERN_02
public class Java_07 {
    public static void main(String[] args) {
        int j,i;
        for( i=0; i<=7;i++)
        {
            for( j=7;j>i;j--)
            {
                System.out.print(" * ");
            }
            System.out.println();
        }
    }
}
