package CHAPTER_05;
// FOR LOOP
// WHEN NUMBER OF ITERATION IS KNOWN WE USE FOR LOOP
public class Java_05 {
    public static void main(String[] args) {
       ////QUICK QUIZ
       for (int i = 1;i<50;i++) {
          int x = 2*i+1;

          System.out.println(x);
       }
       // DECREMENT LOOP
       for (int k=100; k>0;k--)
       {
           System.out.println(k);

        }


    }
}